/**
 * Ship
 */
class Ship {
    constructor(length) {
        this.length = length;
        this.hits = 0;
    }

    /**
     * Activate if the ship has been hit.
     */
    hit() {
        this.hits++;
    }

    /**
     * Tells whether or not the ship has been sunk.
     * @returns true if ship has no more space, otherwise false
     */
    isSunk() {
        return this.length === this.hits;
    }
}

export default Ship;
